package br.com.cris.logica;

public class OperLogico {
	
	public static void main (String[] args) {
		
		int x = 6;
		
		System.out.println((x >= 1) && (x <= 10));
		System.out.println((x >= 1) || (x <= 5));	
		System.out.println( !(x >= 1));
		
	}

}

/** Operadores lógicos
&& and (e)
|| or (ou)
! not (não)

*/
